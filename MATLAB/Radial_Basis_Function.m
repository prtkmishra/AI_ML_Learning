%% Radial Basis Function

X = [0;0];
C1 = [0;0];
C2 = [1;1];
T = [0;1;1;0];
nh = 2;
rowmax = sqrt((C1(1,:)-C2(1,:)).^2+(C1(2,:)-C2(2,:)).^2);
sigma = rowmax/sqrt(2*nh);

d1 = (X(1,:)-C1(1,:))^2+(X(2,:)-C1(2,:))^2;
d2 = (X(1,:)-C2(1,:))^2+(X(2,:)-C2(2,:))^2;

Y1 = exp(-(d1^2/(2*sigma^2)));
Y2 = exp(-(d2^2/(2*sigma^2)));
% Y= [Y1;Y2;1]
Y = [1 0.1353 1; 0.3679 0.3679 1; 0.3679 0.3679 1; 0.1353 1 1]
W = (inv(Y'*Y)*Y')*T